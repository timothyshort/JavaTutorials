package com.TrainingRite.DemoApp;

import java.lang.reflect.Array;

public class Arrays {

	public static void main(String[] args) {
		// Define an Array
		double valueOfNumbers[] = new double[3];
		
		// Assign values to array
		valueOfNumbers[0] = 100;
		valueOfNumbers[1] = 200;
		valueOfNumbers[2] = 300;
		
		// Print value from an array
		System.out.println(valueOfNumbers[0]);
		System.out.println(valueOfNumbers[1]);
		System.out.println(valueOfNumbers[2]);
		
	}

}
